#include <lib6502/cpu.h>
#include <lib6502/makestring.h>

#include <iomanip>

#include "statemacros.h"

using lib6502::Cpu;

// =====================================================================================================================
void Cpu::sei()
{
    traceInstruction("SEI");
    m_status |= IntDisable;
}

// =====================================================================================================================
void Cpu::cli()
{
    traceInstruction("CLI");
    m_status &= ~IntDisable;
}

// =====================================================================================================================
void Cpu::sed()
{
    traceInstruction("SED");
    m_status |= Decimal;
}

// =====================================================================================================================
void Cpu::cld()
{
    traceInstruction("CLD");
    m_status &= ~Decimal;
}

// =====================================================================================================================
void Cpu::sec()
{
    traceInstruction("SEC");
    m_status |= Carry;
}

// =====================================================================================================================
void Cpu::clc()
{
    traceInstruction("CLC");
    m_status &= ~Carry;
}

// =====================================================================================================================
void Cpu::clv()
{
    traceInstruction("CLV");
    m_status &= ~Overflow;
}
